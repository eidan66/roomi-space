interface CustomUseTranslationResponse
  extends Omit<UseTranslationResponse<string, string>, 't'> {
  t: (key: TranslationKeys, options?: Record<string, unknown>) => string;
}

export function useTranslation(): CustomUseTranslationResponse {
  const result = useI18nextTranslation();

  return {
    ...result,
    t: (key: TranslationKeys, options?: Record<string, unknown>): string => {
      const translation = result.t(key, options);
      return typeof translation === 'string' ? translation : String(translation);
    },
  };
}

export default useTranslation;
